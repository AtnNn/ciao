path(X,Y) :- 
	tabled_call(path(X,Y),Sid,'path_Shared:path0','true',Pred),
	(
	    '$meta_call'(Pred)
	;
	    test_complete(Sid)
	),
	is_lider(Sid), %que libere la continuaciÃ³n de llamada!!!
	consume_answer(path(X,Y),Sid).

path0(path(X,Y),Sid) :- 
	edge(X,Z),
	tabled_call(path(Z,Y),NewSid,'path_Shared:path0','path_Shared:path1',Pred),
	(
	    var(Pred) ->
	    true
	;
	    ('$meta_call'(Pred); test_complete(NewSid))
	),
	read_answers(Sid,NewSid,[X,Y,Z],F),
	'$meta_call'(F).

path0(path(X,Y),Sid) :-
	edge(X,Y),
	new_answer(path(X,Y),Sid,F),
	'$meta_call'(F).

path1(path(X,Y),Sid,[Z,Y,X]) :-
	new_answer(path(Z,Y),Sid,F),
	'$meta_call'(F).
    

   
